package javaSort;

public class RowOf2DArray {
	public static void main(String[] args) {
		int[][] arr={{1,2,3},{4,5}};
		System.out.println("arr.length行数"+arr.length);
		System.out.println("arr[0].length列数"+arr[0].length);//输出3
		System.out.println("arr[1].length列数"+arr[1].length);//输出2
	}
}
