package javaSort;

public class PriorityOfMultiplicationAndDivision {
	public static void main(String[] args) {
		float angle = 20;
		float multiFirst = (float) (3.14*angle/180);
		float divisionFirst = (float) (angle/180*3.14);

		System.out.println("multiFirst=" +multiFirst);
		System.out.println("divisionFirst=" +divisionFirst);
	}
}
